    private void Connect() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(url, user, passwd);
            System.out.println("Connected to database");
        } catch (ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void fetchStatistics() {
        try {
            String productsQuery = "SELECT COUNT(*) FROM itemtbl";
            String categoriesQuery = "SELECT COUNT(*) FROM categorytbl";

            PreparedStatement productsStatement = con.prepareStatement(productsQuery);
            PreparedStatement categoriesStatement = con.prepareStatement(categoriesQuery);

            ResultSet productsResult = productsStatement.executeQuery();
            ResultSet categoriesResult = categoriesStatement.executeQuery();

            if (productsResult.next()) {
                totalProducts = productsResult.getInt(1);
            }

            if (categoriesResult.next()) {
                totalCategories = categoriesResult.getInt(1);
            }

            productsStatement.close();
            categoriesStatement.close();
            productsResult.close();
            categoriesResult.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

