    // Method to update the timer label display
    private void updateTimerLabel(Duration duration) {
        long hours = duration.toHours();
        long minutes = duration.toMinutesPart();
        long seconds = duration.toSecondsPart();
        // Assuming you have a label for displaying the timer
        timerLabel.setText(String.format("Total time elapsed: %02d:%02d:%02d", hours, minutes, seconds));
    }

    // Method to start the timer
    private void startTimer() {
        Timer timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                // Calculate elapsed time since application start
                Duration duration = Duration.between(applicationStartTime, Instant.now());
                // Update display
                updateTimerLabel(duration);
            }
        });
        timer.start();
    }
